bmi = 84 / 1.65 ** 2

print(bmi)

# Flooring a number: removing the digits after the decimal
print(int(bmi))

# Rounding a number: rounding up or down based on the first digit after the decimal
print(round(bmi))

# Rounding a number and specifying the number of digits after the decimal
print(round(bmi, 2))

