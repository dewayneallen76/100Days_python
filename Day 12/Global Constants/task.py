# Global Constants

# You can define global constants in your code file for easy access. Their job is
# meant to be "set and forget" so you can use their values but never need to modify
# them.

# Global constants are normally declared in ALL_CAPS with a underscore in between.

PI = 3.14159
GOOGLE_URL = "https://www.google.com"

def my_func():
    print(GOOGLE_URL)

my_func()